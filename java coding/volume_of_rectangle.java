public class volume_of_rectangle {

    public static void main(String[] args) {

        // value for the data
        int length = 15;
        int weidth = 16;
        int height = 47;
        int volume;

        volume = length * weidth * height;

        System.out.println("The lenght of a rectangle : " + length);
        System.out.println("The weidth of a rectangle : " + weidth);
        System.out.println("The height of a rectangle : " + height);
        System.out.println("The volume of a rectangle : " + volume);
    }
}
